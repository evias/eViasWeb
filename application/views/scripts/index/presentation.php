<H1>Développement modulaire</h1>
<p>
Je vous souhaite la bienvenue sur mon serveur web dédié au développement web et logiciel.
</p>

<p>
La plupart des parties de ce site web sont en constante évolution et leur apparence ne
réprésente donc pas l'apparence finale d'un développement officiel. Des versions stables
des différents modules seront publiés tout au long des développements.
</p>

<p>
La séparation en différents modules permet un emploi plus spécialisés des différentes
ressources utilisées par une application web. Ainsi il est important de bien réfléchir
avant même l'implémentation des principes de bases.
</p>

<p>
Le modulage peut devenir très complexe quand il s'agit de logiciels de grande envergure
et il ne devrait jamais trop ralentir le développement d'un logiciel. Il est clair que
développer un catalogue de produits "from scratch" n'est pas toujours simple, car plusieurs
aspects de celui-ci peuvent être diviser en plusieurs sous modules. Ainsi on se retrouve
avec un projet qui est composé d'un nombre plus ou moins important de modules.
</p>

<p>
Certain pourrait penser que cette façon de développer peut porter à une maintenance plus couteuse
et moins effectives. C'est là qu'intervient le point fort des modules, un modules effectue des
accès aux ressources qui lui sont propres. S'il y a un soucis quelque part, il est facile d'en
déduire lequel des modules est en faute. Bien que l'architecture d'un projet s'en retrouve un
rien complexifiée, la maintenance, elle, en devient nettement plus rapide et plus effectives.
</p>

